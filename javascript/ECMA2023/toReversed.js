// reverse()
const reverseTarget = [1, 2, 3, 4, 5];
const reversed = reverseTarget.reverse();

console.log(reverseTarget); // [5, 4, 3, 2, 1]
console.log(reversed); // [5, 4, 3, 2, 1]

// toReversed()
const toReverseTarget = [1, 2, 3, 4, 5];
const toReversed = toReverseTarget.toReversed();

console.log(toReverseTarget); // [1, 2, 3, 4, 5]
console.log(toReversed); // [5, 4, 3, 2, 1]
